export function getCopy(path: string, persona: Persona): string {
  const keys = path.split('.');
  let value: any = personaCopy;

  for (const key of keys) {
    value = value[key];
    if (value === undefined) {
      console.error(`Copy path not found: ${path}`);
      return '';
    }
  }

  if (typeof value === 'object' && 'candidate' in value && 'parent' in value) {
    return value[persona];
  }

  console.error(`Copy path ${path} does not have persona variants`);
  return '';
}
